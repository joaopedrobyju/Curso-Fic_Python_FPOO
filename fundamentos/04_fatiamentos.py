"""
Exemplo de fatiamento de strings em Python
Obs: sempre precisa colocar uma poisção mais
"""

cidade = "Piracicaba"
primeiras_4_letras = cidade[0:4]
print(f"As quatros primeiras letraas da {cidade} são {primeiras_4_letras}")

outras_letras = cidade[2:6]
print(f"Outras letras {outras_letras}")

duas_ultimas_letras = cidade[-2:]
print(f"Duas últimas letras {duas_ultimas_letras}")